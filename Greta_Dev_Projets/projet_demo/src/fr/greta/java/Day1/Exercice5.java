package fr.greta.java;

public class Exercice5 {
    public static void main(String[] args) {
        int a = 5;
        int b = 50;
        System.out.println("a-b = " + (a - b));
        System.out.println("a*b = " + a * b);
        System.out.println("a/b = " + a / b);

        float x = 5.0f;
        float y = 63.5f;
        System.out.println("x-y = " + (x - y));
        System.out.println("x*y = " + x * y);
        System.out.println("x/y = " + x / y);

        System.out.println(45-60);
        System.out.println(45*60);
        System.out.println(45/60);
    }
}
